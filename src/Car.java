public class Car {
    private int weight;
    private String name;
    private int kmchas;

    public int getWeight() {
        return weight;
    }

    public int getKmchas() {
        return kmchas;
    }

    public String getName() {
        return name;
    }

    public void setKmchas(int kmchas) {
        this.kmchas = kmchas;
    }

    public void setName (String name) {
        this.name = name;
    }

    public void setWeight (int weight) {
        this.weight = weight;
    }

}
